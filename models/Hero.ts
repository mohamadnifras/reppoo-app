import mongoose, { Schema } from "mongoose";

const HeroSchema = new Schema(
  {
    title: {
      type: String,
      required: true,
    },
    subtitle: {
      type: String,
      required: true,
    },
    image: {
      type: String,
      required: true,
    },
  },
  { timestamps: true }
);

export default mongoose.models.Hero ||
  mongoose.model("Hero", HeroSchema);
